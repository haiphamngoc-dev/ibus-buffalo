/// Helper function to construct a new `IBusText` without any formatting attributes.
///
/// * `text` - The string slice to wrap.
pub fn new_ibus_text(text: &str) -> IBusText {
    let attr_list = IBusAttrList {
        name: "IBusAttrList".to_string(),
        attachments: HashMap::new(),
        attributes: Vec::new(),
    };
    let attr_list_value = OwnedValue::try_from(attr_list).unwrap();
    IBusText {
        name: "IBusText".to_string(),
        attachments: HashMap::new(),
        text: text.to_string(),
        attr_list: attr_list_value,
    }
}

/// Retrieves the unique machine ID from `/var/lib/dbus/machine-id` or `/etc/machine-id`.
/// Returns an empty string if it cannot be read.
pub fn get_local_machine_id() -> String {
    fs::read_to_string("/var/lib/dbus/machine-id")
        .or_else(|_| fs::read_to_string("/etc/machine-id"))
        .unwrap_or_default()
        .trim()
        .to_string()
}

/// Retrieves the path to the user's config directory, honoring `XDG_CONFIG_HOME`
/// with a fallback to `$HOME/.config`.
pub fn get_user_config_dir() -> PathBuf {
    if let Ok(dir) = env::var("XDG_CONFIG_HOME") {
        PathBuf::from(dir)
    } else {
        let home = env::var("HOME").unwrap_or_default();
        PathBuf::from(home).join(".config")
    }
}

/// Detects the active IBus address by reading the environment variable `IBUS_ADDRESS`
/// or by scanning configuration files generated by IBus for Wayland/X11 sessions.
pub fn get_ibus_address() -> Option<String> {
    if let Ok(addr) = env::var("IBUS_ADDRESS") {
        return Some(addr);
    }

    let machine_id = get_local_machine_id();
    if machine_id.is_empty() {
        return None;
    }

    // Check Wayland
    if let Ok(wayland_display) = env::var("WAYLAND_DISPLAY") {
        let filename = format!("{}-unix-{}", machine_id, wayland_display);
        let path = get_user_config_dir().join("ibus/bus").join(filename);
        if let Ok(content) = fs::read_to_string(path) {
            for line in content.lines() {
                if line.starts_with("IBUS_ADDRESS=") {
                    return Some(line["IBUS_ADDRESS=".len()..].to_string());
                }
            }
        }
    }

    // Check X11
    let display = env::var("DISPLAY").unwrap_or_else(|_| ":0".to_string());
    let display_part = display.split(':').nth(1).unwrap_or("0");
    let display_num = display_part.split('.').next().unwrap_or("0");

    let filename = format!("{}-unix-{}", machine_id, display_num);
    let path = get_user_config_dir().join("ibus/bus").join(filename);
    if let Ok(content) = fs::read_to_string(path) {
        for line in content.lines() {
            if line.starts_with("IBUS_ADDRESS=") {
                return Some(line["IBUS_ADDRESS=".len()..].to_string());
            }
        }
    }

    None
}

