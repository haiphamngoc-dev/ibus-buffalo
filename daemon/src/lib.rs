/// Preedit Input Mode: show composition inline with underlining.
pub const PREEDIT_IM: i32 = 1;
/// Surrounding Text Input Mode: edit surrounding characters directly.
pub const SURROUNDING_TEXT_IM: i32 = 2;
/// Backspace Forwarding Input Mode: delete/retype by forwarding backspace keypress events.
pub const BACKSPACE_FORWARDING_IM: i32 = 3;
/// Shift Left Forwarding Input Mode: delete by simulating Shift+Left Arrow selections.
pub const SHIFT_LEFT_FORWARDING_IM: i32 = 4;
/// Forward as Commit Input Mode: commit text by forwarding synthetic keyboard events.
pub const FORWARD_AS_COMMIT_IM: i32 = 5;
/// XTest Fake Key Event Input Mode: simulate X11 keyboard events directly.
pub const XTEST_FAKE_KEY_EVENT_IM: i32 = 6;
/// US Keyboard layout mode (no Vietnamese processing).
pub const US_IM: i32 = 7;

/// Checks if the input mode relies on simulating backspaces to update the buffer.
pub fn is_backspace_mode(im: i32) -> bool {
    matches!(
        im,
        SURROUNDING_TEXT_IM
            | BACKSPACE_FORWARDING_IM
            | SHIFT_LEFT_FORWARDING_IM
            | FORWARD_AS_COMMIT_IM
            | XTEST_FAKE_KEY_EVENT_IM
    )
}

/// Checks if a keysym represents a modifier key itself.
pub fn is_modifier_key(keyval: u32) -> bool {
    // Standard X11 modifier keysyms:
    // 0xffe1 - 0xffee: Shift, Control, Caps_Lock, Shift_Lock, Meta, Alt, Super, Hyper
    // 0xfe03: ISO_Level3_Shift (AltGr)
    // 0xff7e: Mode_switch
    (keyval >= 0xffe1 && keyval <= 0xffee) || keyval == 0xfe03 || keyval == 0xff7e
}

/// Configuration for the IBus Buffalo input method.
#[derive(Serialize, Deserialize, Debug, Clone)]
pub struct Config {
    /// Currently selected input method (e.g., "Telex", "VNI").
    pub input_method: String,
    /// Engine flags.
    pub flags: u32,
    /// Output charset encoding (e.g., "Unicode", "TCVN3", "VNI", "VIQR").
    pub charset: String,
}

impl Default for Config {
    fn default() -> Self {
        Self {
            input_method: "Telex".to_string(),
            flags: ESTD_FLAGS,
            charset: "Unicode".to_string(),
        }
    }
}

/// Loads configuration from `$XDG_CONFIG_HOME/ibus-buffalo/config.toml`.
/// Fallbacks to `Config::default()` and saves it if the file does not exist.
pub fn load_config() -> Config {
    let path = get_user_config_dir()
        .join("ibus-buffalo")
        .join("config.toml");
    if let Ok(content) = fs::read_to_string(&path) {
        if let Ok(mut cfg) = toml::from_str::<Config>(&content) {
            if cfg.input_method == "English" {
                cfg.input_method = "Telex".to_string();
                let _ = save_config(&cfg);
            }
            return cfg;
        }
    }
    let cfg = Config::default();
    let _ = save_config(&cfg);
    cfg
}

/// Saves configuration to `$XDG_CONFIG_HOME/ibus-buffalo/config.toml`.
pub fn save_config(cfg: &Config) -> std::io::Result<()> {
    let dir = get_user_config_dir().join("ibus-buffalo");
    fs::create_dir_all(&dir)?;
    let path = dir.join("config.toml");
    let content = toml::to_string_pretty(cfg).unwrap();
    fs::write(path, content)?;
    Ok(())
}

